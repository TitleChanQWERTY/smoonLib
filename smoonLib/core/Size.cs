namespace smoonLib.core;

public struct SizeInt
{
    public int X;
    public int Y;

    public SizeInt(int sizeX, int sizeY)
    {
        X = sizeX;
        Y = sizeY;
    }
}

public struct SizeFloat
{
    public float X;
    public float Y;

    public SizeFloat(float sizeX, float sizeY)
    {
        X = sizeX;
        Y = sizeY;
    }
}
