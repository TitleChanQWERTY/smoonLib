namespace smoonLib.core;

public struct SizeInt
{
    public int width;
    public int height;

    public SizeInt(int width, int height)
    {
        this.width = width;
        this.height = height;
    }
}

public struct SizeFloat
{
    public float width;
    public float height;

    public SizeFloat(float width, float height)
    {
        this.width = width;
        this.height = height;
    }
}
